class Lesson_32_Activity_Three{

    //Method prints out the swapped
    public static void swap(int first, int second){
        System.out.println(second + " " + first);
    }

    //Main method swaps the values
    public static void main(String[] args){
        swap(1, 2);
        swap(1, 3);
        swap(1, 4);
        swap(1, 5);
    }
}
